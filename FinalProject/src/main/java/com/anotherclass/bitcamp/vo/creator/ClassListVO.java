package com.anotherclass.bitcamp.vo.creator;

public class ClassListVO {
	private int class_no;
	private String class_name;
	private int request;
	private int classlikecount;
	private int classclasscount;
	private String member_id;
	private String class_thumb;
	
	public int getClass_no() {
		return class_no;
	}
	public void setClass_no(int class_no) {
		this.class_no = class_no;
	}
	public String getClass_name() {
		return class_name;
	}
	public void setClass_name(String class_name) {
		this.class_name = class_name;
	}
	public int getRequest() {
		return request;
	}
	public void setRequest(int request) {
		this.request = request;
	}
	public int getClasslikecount() {
		return classlikecount;
	}
	public void setClasslikecount(int classlikecount) {
		this.classlikecount = classlikecount;
	}
	public int getClassclasscount() {
		return classclasscount;
	}
	public void setClassclasscount(int classclasscount) {
		this.classclasscount = classclasscount;
	}
	public String getMember_id() {
		return member_id;
	}
	public void setMember_id(String member_id) {
		this.member_id = member_id;
	}
	public String getClass_thumb() {
		return class_thumb;
	}
	public void setClass_thumb(String class_thumb) {
		this.class_thumb = class_thumb;
	}
	
	
	
}
